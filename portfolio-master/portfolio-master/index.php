            <h2 class="h2 section-title">  جامعة بني سويف التكنولوجية  </h2>

            <p class="section-text">صدر القرار الجمهوري بإنشاء جامعة بني سويف التكنولوجية في يونيو 2019، بإنشاء الجامعات التكنولوجية، لتعد هذه الخطوة نقلة هامة في استحداث مسار جديد للتعليم الفني في مصر.
              وتعد الجامعات التكنولوجية امتدادا لمسار طلاب التعليم الفني والتي تعمل على إكسابهم المهارات العملية والعلمية لمواكبة متطلبات سوق العمل المحلي والدولي، وذلك من خلال البرامج التكنولوجية التي يتم تطبيقها بالكليات التابعة للجامعة، والتي وضعت بناء على احتياجات المشروعات القومية.
              
             <strong> التعاون الدولي بين الجامعة ودولة كوريا الجنوبية:</strong>
              
              • اتفاقية موقعة بين وزارة التعليم العالي والبحث العلمي ووكالة التعاون الدولي الكورية (كويكا).
              
              • الحصول على الاعتماد لبرامج الجامعة التي تضم برنامج الميكاترونكس Mechatronics وبرنامج تكنولوجيا المعلومات (ICT) لمدة 6 سنوات من عام 2022 إلى عام 2028.
              • تواجد فريق دعم فني من الجامعة الكورية للتكنولوجيا والتعليم (كوريا تك)، ومؤسسة ABEEK المسئولة عن ضمان الجودة والاعتماد للبرامج التكنولوجية بدولة كوريا الجنوبية.
              
              تضم الجامعة  الكلية المصرية الكورية لتكنولوجيا الصناعة والطاقة، ويوجد خمسة برامج وهم :<br>
              
              • برنامج الميكاترونكس. Mechatronics
              • برنامج تكنولوجيا المعلومات. ICT
              • برنامج الأوتوترونكس. Autotronics
              • برنامج الطاقة الجديدة والمتجددة. Renewable Energy
              • برنامج تكنولوجيا الأطراف الصناعية. Prosthetics and Orthotics
               
              
            </p>

            <p class="section-text">
               تمنح جامعة بني سويف التكنولوجية بناءً على اعتماد مجلس الجامعة الدرجات العلمية الآتية:<br>
               • الدبلوم العالي المهني في التكنولوجيا في التخصص. • البكالوريوس المهني في التكنولوجيا في التخصص. • الماجستير المهني في التكنولوجيا في التخصص. • الدكتوراه المهنية في التكنولوجيا في التخصص.
            </p>

